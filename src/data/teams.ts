export const teams: Team[] = teamSeeds.map((seed) => {
  const defaults = teamDefaults[seed.id];
  if (!defaults) {
    throw new Error(`Missing team defaults: ${seed.id}`);
  }

  return {
    id: seed.id,
    name: seed.name,
    shortName: seed.shortName,
    region: seed.region,
    group: seed.group,
    emblemUrl: emblemUrls[`../assets/emblems/teams/${seed.id}.png`] ?? '',
    colors: seed.colors,
    defaultFormationId: defaults.formation,
    defaultTacticId: defaults.tactic,
    ratings: seed.ratings,
    attackers: seed.attackers.map((attacker, index) => ({
      id: `${seed.id}-a${index + 1}`,
      name: `${seed.name}${attacker.name}`,
      number: attacker.number,
      position: attacker.position,
      shooting: attacker.shooting,
      speed: attacker.speed,
      creativity: attacker.creativity,
      finishing: attacker.finishing,
    })),
    goalkeeper: {
      id: `${seed.id}-gk`,
      name: `${seed.name}${seed.goalkeeper.name}`,
      number: seed.goalkeeper.number,
      position: '门将',
      saving: seed.goalkeeper.saving,
      reflex: seed.goalkeeper.reflex,
      stability: seed.goalkeeper.stability,
    },
  };
});

export function getTeam(teamId: string) {
  const team = teams.find((candidate) => candidate.id === teamId);
  if (!team) {
    throw new Error(`Unknown team: ${teamId}`);
  }
  return team;
}

function player(
  name: string,
  number: number,
  position: string,
  shooting: number,
  speed: number,
  creativity: number,
  finishing: number,
): CorePlayerSeed {
  return { name, number, position, shooting, speed, creativity, finishing };
}

function keeper(name: string, number: number, saving: number, reflex: number, stability: number): GoalkeeperSeed {
  return { name, number, saving, reflex, stability };
}
